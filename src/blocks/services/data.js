const addDomainToImages = (data, domain) => {
  for (const category in data) {
    data[category].forEach(item => {
      if (item.image) {
        item.image = `${domain}/${item.image}`;
      }
      if (item.effects) {
        item.effects.forEach(effect => {
          if (effect.image) {
            effect.image = `${domain}/${effect.image}`;
          }
        });
      }
    });
  }
}

const domain = window.location.origin;

addDomainToImages(data, domain);

export default data;